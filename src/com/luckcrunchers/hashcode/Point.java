package com.luckcrunchers.hashcode;

public class Point {
  private int x;
  private int y;

  public Point(int x, int y) {
    this.x = x;
    this.y = y;
  }

  public int distanceFrom(Point p) {
    return Math.abs(p.getX() - this.x) + Math.abs(p.getY() - this.y);
  }

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
